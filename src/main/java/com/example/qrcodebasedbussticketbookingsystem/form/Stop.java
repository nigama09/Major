package com.example.qrcodebasedbussticketbookingsystem.form;

public class Stop {

    private String stopid;
    private String busno;
    private String audiltfair;
    private String childfair;
    private String stopname;

    public String getStopid() {
        return stopid;
    }

    public void setStopid(String stopid) {
        this.stopid = stopid;
    }

    public String getBusno() {
        return busno;
    }

    public void setBusno(String busno) {
        this.busno = busno;
    }

    public String getAudiltfair() {
        return audiltfair;
    }

    public void setAudiltfair(String audiltfair) {
        this.audiltfair = audiltfair;
    }

    public String getChildfair() {
        return childfair;
    }

    public void setChildfair(String childfair) {
        this.childfair = childfair;
    }

    public String getStopname() {
        return stopname;
    }

    public void setStopname(String stopname) {
        this.stopname = stopname;
    }
}
